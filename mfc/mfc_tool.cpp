BOOL CFDIDlg::OnInitDialog()
{
	CButton *pButton;
	CEdit *pEdit;

	// ïŋ―ïŋ―{ïŋ―Nïŋ―ïŋ―ïŋ―X
	CDialog::OnInitDialog();

	// ïŋ―tïŋ―@ïŋ―Cïŋ―ïŋ―ïŋ―ð·ïŋ―
	if (!GetFile()) {
		EndDialog(IDCANCEL);
		return FALSE;
	}

	// Diskïŋ―ïŋ―
	pEdit = (CEdit*)GetDlgItem(IDC_FDI_NAMEE);
	ASSERT(pEdit);
	if (m_dwType == 2) {
		// ïŋ―Åïŋ―ïŋ―Ėtïŋ―@ïŋ―Cïŋ―ïŋ―ïŋ―Iïŋ―ïŋ―ïŋ―ïŋ―D68ïŋ―ïŋ―ïŋ―Iïŋ―Îę―ïŋ―ęïŋ―Ėïŋ―
		_tcscpy(m_szDiskName, _T("Default"));
	}
	pEdit->SetWindowText(m_szDiskName);
	MaskName();

	// Physical format
	SetPhysical();
	MaskPhysical();

	// Logical format
	SetLogical();
	MaskLogical();

	// Drive
	switch (m_nDrive) {
		// ïŋ―hïŋ―ïŋ―ïŋ―Cïŋ―u0
		case 0:
			pButton = (CButton*)GetDlgItem(IDC_FDI_DRIVE0);
			break;

		// ïŋ―hïŋ―ïŋ―ïŋ―Cïŋ―u1
		case 1:
			pButton = (CButton*)GetDlgItem(IDC_FDI_DRIVE1);
			break;

		// Driveïŋ―ïŋ―ïŋ―Čïŋ―
		default:
			pButton = (CButton*)GetDlgItem(IDC_FDI_NOTMOUNT);
			break;
	}
	ASSERT(pButton);
	pButton->SetCheck(1);

	return TRUE;
}

